public class ChartIndex {
    private String ChartIndexOpen;
    private String ChartIndexHigh;
    private String ChartIndexLow;
    private String ChartIndexClose;
    private String ChartIndexVolumn;
    private String ChartIndexTime;

    public ChartIndex() {
    }

    public String getChartIndexOpen() {
        return ChartIndexOpen;
    }

    public void setChartIndexOpen(String chartIndexOpen) {
        this.ChartIndexOpen = chartIndexOpen;
    }

    public String getChartIndexHigh() {
        return ChartIndexHigh;
    }

    public void setChartIndexHigh(String chartIndexHigh) {
        this.ChartIndexHigh = chartIndexHigh;
    }

    public String getChartIndexLow() {
        return ChartIndexLow;
    }

    public void setChartIndexLow(String chartIndexLow) {
        this.ChartIndexLow = chartIndexLow;
    }

    public String getChartIndexClose() {
        return ChartIndexClose;
    }

    public void setChartIndexClose(String chartIndexClose) {
        this.ChartIndexClose = chartIndexClose;
    }

    public String getChartIndexVolumn() {
        return ChartIndexVolumn;
    }

    public void setChartIndexVolumn(String chartIndexVolumn) {
        this.ChartIndexVolumn = chartIndexVolumn;
    }

    public String getChartIndexTime() {
        return ChartIndexTime;
    }

    public void setChartIndexTime(String chartIndexTime) {
        this.ChartIndexTime = chartIndexTime;
    }
}
