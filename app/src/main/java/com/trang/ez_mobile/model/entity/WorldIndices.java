package com.trang.ez_mobile.model.entity;

public class WorldIndices {
    private String titleWord;
    private String lastWord;
    private String changWord;
    private String changRatio;

    public String getTitleWord() {
        return titleWord;
    }

    public void setTitleWord(String titleWord) {
        this.titleWord = titleWord;
    }

    public String getLastWord() {
        return lastWord;
    }

    public void setLastWord(String lastWord) {
        this.lastWord = lastWord;
    }

    public String getChangWord() {
        return changWord;
    }

    public void setChangWord(String changWord) {
        this.changWord = changWord;
    }

    public String getChangRatio() {
        return changRatio;
    }

    public void setChangRatio(String changRatio) {
        this.changRatio = changRatio;
    }
}
